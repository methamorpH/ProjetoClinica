    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        if(jTextFieldUsuario.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Por favor, preencha o campo 'Usuário'");
            jTextFieldUsuario.requestFocus();
        }
        else if(jPasswordFieldSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, preencha o campo 'Senha'");
            jPasswordFieldSenha.requestFocus();
        }
        else if(jPasswordFieldConfirmarSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, preencha o campo 'Confirmar Senha'");
            jPasswordFieldConfirmarSenha.requestFocus();
        }
        else if(!jPasswordFieldSenha.getText().equals(jPasswordFieldConfirmarSenha.getText())){
            JOptionPane.showMessageDialog(null, "As senhas inseridas não conferem!");
            jPasswordFieldConfirmarSenha.requestFocus();
        }

        else if(flagSalvar == 1) {
            mod.setNome(jTextFieldUsuario.getText());
            mod.setSenha(jPasswordFieldSenha.getText());
            mod.setTipo((String) jComboBoxTipo.getSelectedItem());
            control.salvar(mod);
            jTextFieldUsuario.setText("");
            jPasswordFieldSenha.setText("");
            jPasswordFieldConfirmarSenha.setText("");
            jTextFieldPesquisar.setText("");
            jComboBoxTipo.setEnabled(false);
            jTextFieldUsuario.setEnabled(false);
            jPasswordFieldSenha.setEnabled(false);
            jPasswordFieldConfirmarSenha.setEnabled(false);
            jComboBoxTipo.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            jButtonNovo.setEnabled(true);
            jButtonPesquisar.setEnabled(true);
            jTextFieldPesquisar.setEnabled(true);
            jButtonAtualizar.setEnabled(true);
            preencherTabela("SELECT * FROM public.usuarios ORDER BY usu_nome");
        }else{
            mod.setNome(jTextFieldUsuario.getText());
            mod.setSenha(jPasswordFieldSenha.getText());
            mod.setTipo((String) jComboBoxTipo.getSelectedItem());
            mod.setCodigo(Integer.parseInt(jTextFieldId.getText()));
            control.Editar(mod);
            jTextFieldId.setText("");
            jTextFieldUsuario.setText("");
            jPasswordFieldSenha.setText("");
            jPasswordFieldConfirmarSenha.setText("");
            jTextFieldPesquisar.setText("");
            jTextFieldUsuario.setEnabled(false);
            jPasswordFieldSenha.setEnabled(false);
            jPasswordFieldConfirmarSenha.setEnabled(false);
            jComboBoxTipo.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            jButtonNovo.setEnabled(true);
            jTextFieldPesquisar.setEnabled(true);
            jButtonPesquisar.setEnabled(true);
            jButtonAtualizar.setEnabled(true);
            preencherTabela("SELECT * FROM public.usuarios ORDER BY usu_nome");
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

