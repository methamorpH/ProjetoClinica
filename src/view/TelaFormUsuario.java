    private void jTableUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableUsuariosMouseClicked
        // TODO add your handling code here:
        String id_usuario = ""+jTableUsuarios.getValueAt(jTableUsuarios.getSelectedRow(), 0);
        connect.conectarBD();
        connect.executarSql("SELECT * FROM usuarios WHERE usu_codigo = '"+id_usuario+"'");
        try {
            connect.rs.first();
            jTextFieldId.setText(String.valueOf(connect.rs.getInt("usu_codigo")));
            jTextFieldUsuario.setText(connect.rs.getString("usu_nome"));
            jPasswordFieldSenha.setText(connect.rs.getString("usu_senha"));
            jPasswordFieldConfirmarSenha.setText(connect.rs.getString("usu_senha"));
            jComboBoxTipo.setSelectedItem(connect.rs.getString("usu_tipo"));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao buscar registros no banco de dados: "+ex.getMessage());
        }
        connect.desconectarBD();
        jButtonNovo.setEnabled(false);
        jButtonAlterar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        jButtonCancelar.setEnabled(true);
        jButtonSalvar.setEnabled(false);
        jTextFieldId.setEnabled(false);
        jTextFieldUsuario.setEnabled(false);
        jPasswordFieldSenha.setEnabled(false);
        jPasswordFieldConfirmarSenha.setEnabled(false);
        jComboBoxTipo.setEnabled(false);
                
    }//GEN-LAST:event_jTableUsuariosMouseClicked

   public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String[] colunas = new String[] {"ID", "Usuário", "Tipo"};
        connect.conectarBD();
        connect.executarSql(Sql);
        try {
            connect.rs.first();
                do{
                    dados.add(new Object[] {connect.rs.getInt("usu_codigo"), connect.rs.getString("usu_nome"), connect.rs.getString("usu_tipo")});
                    
                }while(connect.rs.next());
        } catch (SQLException ex) {
            //JOptionPane.showMessageDialog(null, "Ocorreu um erro ao buscar registros no banco de dados: "+ex.getMessage());
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTableUsuarios.setModel(modelo);
        jTableUsuarios.getColumnModel().getColumn(0).setPreferredWidth(23);
        jTableUsuarios.getColumnModel().getColumn(0).setResizable(false);
        jTableUsuarios.getColumnModel().getColumn(1).setPreferredWidth(180);
        jTableUsuarios.getColumnModel().getColumn(1).setResizable(false);
        jTableUsuarios.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTableUsuarios.getColumnModel().getColumn(2).setResizable(false);
        jTableUsuarios.getTableHeader().setReorderingAllowed(false);
        jTableUsuarios.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
        jTableUsuarios.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        connect.desconectarBD();
    }
